def compute(s: str) -> int:
    cycle = 0
    X = 1
    result = []
    lines = s.splitlines()
    for line in lines:
        if line == 'noop':
            cycle += 1
            if cycle in [220,180,100,140,60,20]:
                result.append(X * cycle)

            #noop action starts
        else: # addx
            for i in range(2):
                cycle += 1

                if cycle in [220,180,100,140,60,20]:
                    result.append(X * cycle)

            #addx action starts
            X += int(line.split()[1])
    print(f'Results = {result}')
    return sum(result)


